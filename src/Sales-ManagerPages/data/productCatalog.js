// Search products by code or description
export const searchProducts = (query) => {
  if (!query || !query.trim()) return PRODUCT_CATALOG;
  const q = query.toLowerCase().trim();
  return PRODUCT_CATALOG.filter(
    p => p.code.toLowerCase().includes(q) || 
         p.description.toLowerCase().includes(q)
  );
};

// Get product by exact code
export const getProductByCode = (code) => {
  return PRODUCT_CATALOG.find(p => p.code === code);
};

// Get products by category
export const getProductsByCategory = (category) => {
  if (category === "all") return PRODUCT_CATALOG;
  return PRODUCT_CATALOG.filter(p => p.category === category);
};

// Get product count
export const getProductCount = () => PRODUCT_CATALOG.length;

// Get category with counts
export const getCategoriesWithCounts = () => {
  return PRODUCT_CATEGORIES.map(cat => ({
    name: cat,
    count: PRODUCT_CATALOG.filter(p => p.category === cat).length
  }));
};
